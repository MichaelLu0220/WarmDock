/**
 * Footer 顯示用的短句池。
 * 每次打開 panel 隨機抽一句。
 *
 * 設計原則:
 * - 短(中文 6~14 字),不搶主流程風頭
 * - 語氣偏溫暖/內省,避免純成就口吻或業績話術
 * - 不用驚嘆號,不用「加油」「必勝」這種硬字
 */
const MANTRAS: string[] = [
  "一步一步走。",
  "今天不用完美。",
  "承諾不是清單,是選擇。",
  "小事也算數。",
  "慢慢來也來得及。",
  "每天都是新的起點。",
  "你已經在路上了。",
  "做得到的,就做。",
  "不必跟昨天比。",
  "走多遠都好,別忘了方向。",
  "今天值得被記得。",
  "節奏是自己的。",
  "靜靜完成,就夠了。",
  "一件一件來。",
  "你不必全部都扛。",
  "今天的你也很好。",
];

/**
 * 隨機抽一句 mantra。
 * 每次呼叫都獨立抽籤,讓 Footer 在 panel 每次打開時都有新意。
 */
export function pickRandomMantra(): string {
  const idx = Math.floor(Math.random() * MANTRAS.length);
  return MANTRAS[idx];
}